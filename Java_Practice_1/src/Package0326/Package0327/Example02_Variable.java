package Package0327;

public class Example02_Variable {
	
	public static void main(String[] args) {
		// 두 개의 정수를 저장하기 위한 변수를 선언하여
		// 20, 50을 저장한 다음 합을 출력하는 코드를 작성
		
		int a = 20, b = 50;
		int sum = 0;
		sum = a + b;

		System.out.println(a + " + " + b + "의 합은 : " + sum);
		
		// 같은 타입의 변수들을 한 번에 선언/초기화 할 수 있다
		int n3, n4;
		int n5 = 50, n6 = 20;
	}
}
